    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        if (btnNew.getText().equals("Baru")) {
txtNim.setText(""); txtName.setText(""); txtAddress.setText(""); txtEmail.setText(""); txtNim.setEditable(true); txtName.setEditable(true); txtAddress.setEditable(true); txtEmail.setEditable(true); txtNim.requestFocus(); btnNew.setText("Simpan"); btnDelete.setText("Batal"); btnEdit.setEnabled(false); btnPrev.setEnabled(false); btnNext.setEnabled(false);
} else {
if (!txtNim.getText().equals("") && !txtName.getText().equals("")) {
StudentManager studentmgr = new StudentManager(); //buka koneksi 
Student s = new Student();
s.setNim(txtNim.getText()); s.setName(txtName.getText()); s.setAddress(txtAddress.getText()); s.setEmail(txtEmail.getText());
                if (studentmgr.Insert(s) > 0) {
                    loadData();
                    currentRow = students.size() - 1;
bindData(); JOptionPane.showMessageDialog(this, "Data berhasildisimpan", "Informasi", JOptionPane.INFORMATION_MESSAGE); } else {
JOptionPane.showMessageDialog(this, "Data gagal disimpan","Informasi", JOptionPane.INFORMATION_MESSAGE);
}
txtNim.setEditable(false); txtName.setEditable(false); txtAddress.setEditable(false); txtEmail.setEditable(false); btnNew.setText("Baru"); btnDelete.setText("Hapus"); btnEdit.setEnabled(true); btnPrev.setEnabled(true); btnNext.setEnabled(true); studentmgr.closeConnection(); //tutup koneksi
} else {
JOptionPane.showMessageDialog(this, "Mohon isi data dengan benar", "Informasi", JOptionPane.INFORMATION_MESSAGE); }
}
    }//GEN-LAST:event_btnNewActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (btnEdit.getText().equals("Ubah")) {
txtNim.setEditable(true); txtName.setEditable(true); txtAddress.setEditable(true); txtEmail.setEditable(true); btnEdit.setText("Simpan"); btnDelete.setText("Batal"); btnNew.setEnabled(false); btnPrev.setEnabled(false); btnNext.setEnabled(false);
} else {
StudentManager studentmgr = new StudentManager(); Student s = students.get(currentRow); s.setNim(txtNim.getText()); s.setName(txtName.getText()); s.setAddress(txtAddress.getText()); s.setEmail(txtEmail.getText());
if (studentmgr.Update(s) > 0) {
loadData();
bindData();
JOptionPane.showMessageDialog(this, "Data berhasil diupdate", "Information", JOptionPane.INFORMATION_MESSAGE); } else {
JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Information", JOptionPane.INFORMATION_MESSAGE);
}
txtNim.setEditable(false); txtName.setEditable(false); txtAddress.setEditable(false); txtEmail.setEditable(false); btnEdit.setText("Ubah"); btnDelete.setText("Hapus"); btnNew.setEnabled(true); btnPrev.setEnabled(true); btnNext.setEnabled(true);
}
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (btnDelete.getText().equals("Hapus")) {
int dialog = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin akan menghapus data ini?",
"Konfirmasi", JOptionPane.YES_NO_OPTION); StudentManager studentmgr = new StudentManager(); //buka
Student s = students.get(currentRow); if(dialog == JOptionPane.YES_OPTION){ if (studentmgr.Delete(s) > 0) {
loadData();
currentRow = currentRow - 1;
bindData();
JOptionPane.showMessageDialog(this, "Data berhasil dihapus","Informasi", JOptionPane.INFORMATION_MESSAGE); } else {
JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Informasi", JOptionPane.INFORMATION_MESSAGE);
}
}
studentmgr.closeConnection(); //tutup koneksi
} else if (btnDelete.getText().equals("Batal")) { loadData();
bindData(); btnNew.setText("Baru"); btnEdit.setText("Ubah"); btnDelete.setText("Hapus"); btnEdit.setEnabled(true); btnNew.setEnabled(true); btnPrev.setEnabled(true); btnNext.setEnabled(true); txtNim.setEditable(false); txtName.setEditable(false); txtAddress.setEditable(false); txtEmail.setEditable(false);
}
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevActionPerformed
        // TODO add your handling code here:
        if (currentRow > 0) {
            --currentRow;
            btnNext.setEnabled(true);
        } else {
btnPrev.setEnabled(false); }
        bindData();
    }//GEN-LAST:event_btnPrevActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        if (currentRow < students.size() - 1) {
            ++currentRow;
            btnPrev.setEnabled(true);
        } else {
            btnNext.setEnabled(false);
        }
        bindData();
    }//GEN-LAST:event_btnNextActionPerformed

