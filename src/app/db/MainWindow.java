    private void bindData() {
        if (students.size() > 0) {
            Student s = students.get(currentRow);
            txtNim.setText(s.getNim());
            txtName.setText(s.getName());
            txtAddress.setText(s.getAddress());
            txtEmail.setText(s.getEmail());
        } else {
            currentRow = 0;
            txtNim.setText("");
            txtName.setText("");
            txtAddress.setText("");
            txtEmail.setText("");
        }
    }
